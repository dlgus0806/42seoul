int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (i <= nb / i)
	{
		if ((nb % i == 0) && (nb / i) == i)
			return (i);
		i++;
	}
	return (0);
}

#include <stdio.h>

int main()
{
	printf("%d", ft_sqrt(2147395600));
}
